    public function edit($id)
    {
        $user = User::find($id);
        if($user->role_id != 3){
            $user->role_id = 3;
            $message = "$user->pseudo a été nommé Admin";
        }
        elseif($user->role_id === 3){
            $user->role_id = 2;
            $message = "$user->pseudo a été supprimé des Admins";
        }
        $user->save();
        return redirect(route('users.index'))->with('message',$message);
    }

